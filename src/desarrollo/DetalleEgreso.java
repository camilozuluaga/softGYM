    private void cargarEgresos() {
        CachedRowSet data;

        try {
            DefaultTableModel tableModel = new DefaultTableModel(null, new String[]{"ID", "Descripcion Del Egreso", "Valor del Egreso", "Hora Del Egreso"});
            data = db.sqlDatos("SELECT mov.id, mov.nota, mov.valor, mov.fecha_registro FROM movimiento mov, caja ca WHERE tipo= 'Egreso' AND ca.fecha_apertura='".concat(utilidades.fecha_apertura()).concat("'").concat(" AND mov.fecha_registro >= '").concat(utilidades.fecha_apertura()).concat("'"));

            tablaEgresos = logica.Utilidades.llenarTabla(data.createCopy(), tableModel, tablaEgresos);

        } catch (Exception ex) {
            Logger.getLogger(RegistrarPagoMembresia.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
