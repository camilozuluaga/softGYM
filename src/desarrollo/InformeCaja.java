    public void descuentosSocios() {
        CachedRowSet data;
        DefaultTableModel tableModel = new DefaultTableModel(null, new String[]{"NOMBRE", "APELLIDO", "VALOR DESCUENTO", "DESCRIPCIÓN DESCUENTO", "FECHA DESCUENTO"});
        try {

            String querySQL = "SELECT CONCAT(so.primer_nombre || ' ' || so.segundo_nombre) AS Nombre, CONCAT(so.primer_apellido || ' ' || so.segundo_apellido) AS Apellido, REPLACE(CAST(md.descuento as text), '.0', '') AS Descuento, md.descuento_observacion, md.fecha_registro\n"
                    + "FROM membresia_datos md , membresia_usuario mu, socio so, membresia m\n"
                    + "WHERE mu.socio_id = so.id\n"
                    + "AND mu.membresia_id = m.id\n"
                    + "AND md.descuento > 0.0\n"
                    + "AND md.membresia_socio_id = mu.id\n"
                    + "ORDER BY md.id DESC";
            System.out.println("La consulta es: " + querySQL);
            data = db.sqlDatos(querySQL);
            
            if(data.size() ==0){
                JOptionPane.showMessageDialog(null, "No se encontro ningun descuento", "Mensaje de Información", JOptionPane.INFORMATION_MESSAGE);
            }

            tabla = logica.Utilidades.llenarTabla(data.createCopy(), tableModel, tabla);
        } catch (SQLException ex) {
            Logger.getLogger(InformeCaja.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public String obtnerFechaActual() {

        Calendar fechaActual = new GregorianCalendar();

        String diaa = String.valueOf(fechaActual.get(Calendar.DAY_OF_MONTH));
        String mess = String.valueOf(fechaActual.get(Calendar.MONTH) + 1);
        String anoo = String.valueOf(fechaActual.get(Calendar.YEAR));
        String hora = String.valueOf(fechaActual.get(Calendar.HOUR));
        String minutos = String.valueOf(fechaActual.get(Calendar.MINUTE));
        String segundos = String.valueOf(fechaActual.get(Calendar.SECOND));
        String mili = String.valueOf(fechaActual.get(Calendar.MILLISECOND));
        String fecha = anoo + "-" + mess + "-" + diaa + " " + hora + "" + minutos + "" + segundos + "" + mili;
        return fecha;
    }

