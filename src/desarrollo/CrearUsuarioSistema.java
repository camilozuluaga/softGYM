    public void tablaMenus(){
        try {
            //Si usuario aun no se ha crado darle valor de 0 para que solo traiga los menus disponibles
            String usuario = "0";
            if(usuarioId != null){
                usuario = usuarioId;
            }
            CachedRowSet data;
            DefaultTableModel tableModel = (DefaultTableModel) tablaMenus.getModel();
            data = db.sqlDatos("SELECT menu.*, (SELECT permitir FROM sistema_menu_usuario WHERE usuario_id = "+usuario+" AND menu_id = menu.id ) AS Permitir FROM sistema_menu menu");
            tablaMenus = logica.Utilidades.llenarTablaPermisos(data.createCopy(), tableModel, tablaMenus);
        } catch (SQLException ex) {
            Logger.getLogger(CrearUsuarioSistema.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    private void crearPermisos() {
        crearPermisosMenus();
    }
    
    private void actualizarPermisos(){
        actualizarPermisosMenu();
    }

    private void crearPermisosMenus() {
        int registros = tablaMenus.getRowCount();
        for (int i = 0; i < registros; i++) {
            acceso.crearPermisoMenu(usuarioId, tablaMenus.getValueAt(i, 0), tablaMenus.getValueAt(i, 3));
        }
    }
    
    private void actualizarPermisosMenu(){
        int registros = tablaMenus.getRowCount();
        for (int i = 0; i < registros; i++) {
            
            Object menuId = tablaMenus.getValueAt(i, 0);
            Object permitir = tablaMenus.getValueAt(i, 3);
            
            if(acceso.existePermisoMenu(usuarioId, menuId)){
                acceso.actualizarPermisoMenu(usuarioId, menuId, permitir);
            }else{
                acceso.crearPermisoMenu(usuarioId, menuId, permitir);
            }
        }
    }

