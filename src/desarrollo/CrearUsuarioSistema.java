    public void traerDatos(String id) {
        CachedRowSet data;
        String querySQL;
        querySQL = String.format("SELECT us.estado,us.identificacion,us.primer_nombre,us.segundo_nombre,us.primer_apellido,us.segundo_apellido,us.clave,us.fecha_nacimiento,us.email,us.telefono,us.sexo,us.empleado,us.estado,us.foto , us.admin \n"
                + "FROM usuario_sistema us\n"
                + "WHERE us.id= '%s';", id);
        data = db.sqlDatos(querySQL);
        try {
            while (data.next()) {
                estadoBD = data.getBoolean("estado");
                if (id.equals("1")) {
                    jcEmpleado.setVisible(false);
                } else {
                    if (data.getBoolean("estado") == true) {
                        jcEmpleado.setText("Desactivar");
                    } else {
                        jcEmpleado.setText("Activar");
                    }
                }
                txtClave.setText(data.getString("clave"));
                txtIdentificacion.setText(data.getString("identificacion"));
                txtPrimerNombre.setText(data.getString("primer_nombre"));
                txtSegundoNombre.setText(data.getString("segundo_nombre"));
                txtPrimerApellido.setText(data.getString("primer_apellido"));
                txtSegundoApellido.setText(data.getString("segundo_apellido"));
                jcEmpleado.setSelected(data.getBoolean("empleado"));
                cAdministador.setSelected(data.getBoolean("admin"));
                Date fechaNacimiento = data.getDate("fecha_nacimiento");
                if (fechaNacimiento != null) {
                    jdFechaNaci.setDate(fechaNacimiento);
                }
                txtCorreo.setText(data.getString("email"));
                cboSexo.setSelectedItem(data.getString("sexo"));
                if (data.getBytes("foto") != null) {
                    System.out.println(data.getBytes("foto"));
                    ImageIcon foto = new ImageIcon(data.getBytes("foto"));
                    lblFoto.setIcon(foto);
                }
                txtTelefono.setText(data.getString("telefono"));
                if (data.getBoolean("estado") == false) {
                    jcEmpleado.setText("Activar");
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(RegistrarPagoMembresia.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public int buscarId(String usarioId) {
        CachedRowSet data;
        int id = 0;
        String querySQL = String.format("SELECT id FROM usuario_sistema WHERE identificacion = '%s' ", usarioId);
        data = db.sqlDatos(querySQL);
        try {
            if (data.next()) {
                id = data.getInt("id");
                return id;
            }
        } catch (SQLException ex) {
            Logger.getLogger(RegistrarPagoMembresia.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id;
    }
    
    
