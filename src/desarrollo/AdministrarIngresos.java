    public void cargarIngresos() {
        CachedRowSet data;
        try {

            DefaultTableModel tableModel = new DefaultTableModel(null, new String[]{"ID", "Descripcion Del Ingreso", "Valor del Ingreso", "Hora Del Ingreso"});
            data = db.sqlDatos("SELECT id, nota, valor, fecha_registro FROM movimiento WHERE tipo= 'Ingreso'");

            tablaIngresos = logica.Utilidades.llenarTabla(data.createCopy(), tableModel, tablaIngresos);

        } catch (Exception ex) {
            Logger.getLogger(RegistrarPagoMembresia.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
